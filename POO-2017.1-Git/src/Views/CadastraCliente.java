    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed

        Cliente cliente = new Cliente();
        boolean podeCadastrar = true;
        ArrayList<String> campos = new ArrayList<String>();

        if (this.cpfTxt.getText().equals("")) {
            campos.add("CPF");
            podeCadastrar = false;
        } else {
            try{
                Long.parseLong(cpfTxt.getText());
                cliente.setCpf(this.cpfTxt.getText());
            }catch(NumberFormatException e){
                podeCadastrar = false;
                JOptionPane.showMessageDialog(this, "S√≥ podem haver n√∫meros no CPF");
            }
        }

        if (this.dataTxt.getText().equals("")) {
            campos.add("Data de Nascimento");
            podeCadastrar = false;
        } else {
            cliente.setDataNascimento(this.dataTxt.getText());
        }

        if (this.emailTxt.getText().equals("")) {
            campos.add("Email");
            podeCadastrar = false;
        } else {
            cliente.setEmail(this.emailTxt.getText());
        }

        cliente.setGenero(this.generoCombo.getItemAt(this.generoCombo.getSelectedIndex()));

        if (this.nomeTxt.getText().equals("")) {
            campos.add("Nome");
            podeCadastrar = false;
        } else {
            cliente.setNome(this.nomeTxt.getText());
        }

        if (this.rgTxt.getText().equals("")) {
            campos.add("RG");
            podeCadastrar = false;
        } else {
            try{
                Long.parseLong(rgTxt.getText());
                cliente.setRg(this.rgTxt.getText());
            }catch(NumberFormatException e){
                podeCadastrar = false;
                JOptionPane.showMessageDialog(this, "S√≥ podem haver n√∫meros no RG.");
        }
            }

        cliente.setStatusDevedor(false);

        cliente.setTipo("NORMAL");

        if (this.numeroTxt.getText().equals("")) {
            campos.add("DDD");
            podeCadastrar = false;
        }
        if (this.dddTxt.getText().equals("")) {
            campos.add("N√∫mero de telefone");
            podeCadastrar = false;
        }
        Telefone tel = new Telefone(this.dddTxt.getText(), this.numeroTxt.getText());
        cliente.setTelefone(tel);

        if (this.logradouroTxt.getText().equals("")) {
            campos.add("Logradouro");
            podeCadastrar = false;
        }
        if (this.bairroTxt.getText().equals("")) {
            campos.add("Bairro");
            podeCadastrar = false;
        }
        if (this.numeroEnderecoTxt.getText().equals("")) {
            campos.add("N√∫mero de Endere√ßo");
            podeCadastrar = false;
        }
        if (this.cepTxt.getText().equals("")) {
            campos.add("CEP");
            podeCadastrar = false;
        }
        if (this.cidadeTxt.getText().equals("")) {
            campos.add("Cidade");
            podeCadastrar = false;
        }

        Endereco end = new Endereco(this.logradouroTxt.getText(), this.bairroTxt.getText(),
                this.numeroEnderecoTxt.getText(), this.cepTxt.getText(), 
                this.estadoCombo.getItemAt(this.estadoCombo.getSelectedIndex()), this.cidadeTxt.getText());
        cliente.setEndereco(end);

        try {
            if (podeCadastrar) {
                sys.cadastrarCliente(cliente);
                JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
                this.dispose();
            } else {
                if (campos.size() > 1) {
                    JOptionPane.showMessageDialog(this, "Voc√™ deixou os seguintes campos vazios:  \n" + campos.toString());
                } else {
                    JOptionPane.showMessageDialog(this, "Voc√™ deixou o seguinte campo vazio: \n" + campos.toString());
                }
            }
        } catch (ClienteJaExistenteException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

        
    }//GEN-LAST:event_confirmarButtonActionPerformed

